import { useEffect, useRef, useState } from "react";
import { RefreshCw, Check, X, ArrowRight } from "lucide-react";
import { Button } from "@/components/atoms/button";
import { Badge } from "@/components/atoms/badge";
import { Card, CardDescription, CardHeader, CardTitle } from "@/components/atoms/card";
import { PageHeader } from "@/components/molecules/PageHeader";
import { cn } from "@/lib/utils";
import { TRACKERS_COPY } from "@/content/trackers";
import type { PromptDto } from "@/types/api";
import {
  usePrompts,
  useGeneratePrompts,
  useConfirmPrompts,
  useRemovePrompt,
} from "../hooks/usePrompts";

interface PromptReviewScreenProps {
  trackerId: string;
}

export function PromptReviewScreen({ trackerId }: PromptReviewScreenProps) {
  const prompts = usePrompts(trackerId);
  const generate = useGeneratePrompts();
  const confirm = useConfirmPrompts(trackerId);
  const removePrompt = useRemovePrompt(trackerId);
  const [confirmedCount, setConfirmedCount] = useState<number | null>(null);
  const autoGenerated = useRef(false);

  // Fresh tracker (no prompts yet) → generate once on first load.
  useEffect(() => {
    if (
      !autoGenerated.current &&
      prompts.isSuccess &&
      prompts.data.count === 0 &&
      !generate.isPending
    ) {
      autoGenerated.current = true;
      generate.mutate(trackerId);
    }
  }, [prompts.isSuccess, prompts.data, generate, trackerId]);

  if (confirmedCount !== null) {
    return (
      <div className="flex min-h-[400px] items-center justify-center p-4">
        <Card className="w-full max-w-md text-center">
          <CardHeader>
            <div className="mx-auto mb-2 flex h-12 w-12 items-center justify-center rounded-full bg-semantic-success-100 text-semantic-success-600">
              <Check className="h-6 w-6" />
            </div>
            <CardTitle>{TRACKERS_COPY.review.confirmedTitle}</CardTitle>
            <CardDescription>
              {TRACKERS_COPY.review.confirmedDescription.replace("{count}", String(confirmedCount))}
            </CardDescription>
          </CardHeader>
        </Card>
      </div>
    );
  }

  if (prompts.isLoading || generate.isPending) {
    return (
      <div className="flex min-h-[400px] flex-col items-center justify-center gap-3 p-4 text-center">
        <RefreshCw className="h-6 w-6 animate-spin text-primary-600" />
        <p className="text-sm text-neutral-500">{TRACKERS_COPY.review.generating}</p>
      </div>
    );
  }

  if (!prompts.data) return null;
  const data = prompts.data;

  const groups = new Map<string, PromptDto[]>();
  for (const prompt of data.prompts) {
    const group = groups.get(prompt.visibilityCheckName) ?? [];
    group.push(prompt);
    groups.set(prompt.visibilityCheckName, group);
  }

  function handleConfirm() {
    confirm.mutate(undefined, { onSuccess: (res) => setConfirmedCount(res.activatedCount) });
  }

  return (
    <div className="mx-auto max-w-2xl p-4">
      <PageHeader title={TRACKERS_COPY.review.title} description={TRACKERS_COPY.review.description}>
        <div className="flex items-center gap-2">
          <span className="text-sm tabular-nums text-neutral-500">
            {TRACKERS_COPY.review.allocation
              .replace("{count}", String(data.count))
              .replace("{allocation}", String(data.promptAllocation))}
          </span>
          <Button
            variant="outline"
            size="sm"
            className="gap-1"
            disabled={generate.isPending}
            onClick={() => generate.mutate(trackerId)}
          >
            <RefreshCw className={cn("h-3.5 w-3.5", generate.isPending && "animate-spin")} />
            {TRACKERS_COPY.review.regenerate}
          </Button>
        </div>
      </PageHeader>

      {data.count === 0 ? (
        <div className="mt-6 rounded-lg border border-dashed border-neutral-300 p-10 text-center">
          <p className="text-sm text-neutral-500">{TRACKERS_COPY.review.empty}</p>
          <Button className="mt-4" onClick={() => generate.mutate(trackerId)}>
            {TRACKERS_COPY.review.generate}
          </Button>
        </div>
      ) : (
        <div className="mt-4 space-y-5">
          {[...groups.entries()].map(([checkName, items]) => (
            <div key={checkName}>
              <div className="mb-2 text-[11px] font-medium uppercase tracking-wide text-neutral-400">
                {checkName}
              </div>
              <ul className="space-y-2">
                {items.map((prompt) => (
                  <li
                    key={prompt.id}
                    className="group flex items-start justify-between gap-3 rounded-lg border border-neutral-200 bg-surface-card p-3 transition-all hover:border-neutral-300 hover:shadow-sm"
                  >
                    <div className="min-w-0">
                      <p className="text-sm text-neutral-900">{prompt.text}</p>
                      <div className="mt-1 flex flex-wrap items-center gap-1.5">
                        {prompt.primaryTopicName && (
                          <Badge variant="secondary">{prompt.primaryTopicName}</Badge>
                        )}
                        {prompt.source === "UserAdded" && (
                          <Badge variant="secondary">{TRACKERS_COPY.review.custom}</Badge>
                        )}
                      </div>
                    </div>
                    <button
                      type="button"
                      onClick={() => removePrompt.mutate(prompt.id)}
                      aria-label={`Remove prompt: ${prompt.text}`}
                      className="shrink-0 rounded-md p-1 text-neutral-400 opacity-0 transition-all hover:bg-neutral-100 hover:text-neutral-600 focus-visible:opacity-100 focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-neutral-300 group-hover:opacity-100 pointer-coarse:opacity-100"
                    >
                      <X className="h-4 w-4" />
                    </button>
                  </li>
                ))}
              </ul>
            </div>
          ))}

          <Button
            onClick={handleConfirm}
            disabled={confirm.isPending}
            className="w-full gap-2"
            size="lg"
          >
            {confirm.isPending ? TRACKERS_COPY.review.confirming : TRACKERS_COPY.review.confirm}
            {!confirm.isPending && <ArrowRight className="h-4 w-4" />}
          </Button>
        </div>
      )}
    </div>
  );
}
